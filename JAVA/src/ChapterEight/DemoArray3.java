package ChapterEight;

public class DemoArray3
{
    public static void main(String[] args)
    {
        double[] salary = {6.25, 6.55, 10.25, 16.85};

        System.out.println("Salaries one by one are:");

        for (double var : salary)
        {
            System.out.println(var);
        }
    }
}
